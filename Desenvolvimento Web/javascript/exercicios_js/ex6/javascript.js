/*aluno 1*/
let a1n1 = Number(prompt("Aluno 1: Digite sua nota 1: "));
let a1n2 = Number(prompt("Aluno 1: Digite sua nota 2: "));

let med1 = (a1n1+a1n2)/2;

if(med1<=3.0){
    alert("Aluno 1: Média: " + med1 + " .Situação: Reprovado");
}
else if(med1>3.0 && med1<=7.0){
    alert("Aluno 1: Média: " + med1 + " .Situação: Exame");

}else{
    alert("Aluno 1: Média: " + med1 + " .Situação: Aprovado")
}


